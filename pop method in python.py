#---pop() mehtod---
#this method is used to remove last element from the existiing list.
a=[10,20,30,10,90,"geekyshows"]
print("Before pop: ",a)
b=a.pop()
print("After pop: ",a)
print(b)

#---pop(n) method---
#this method is used to remove an element specified by position number, from the existing list and returns removed elements.
c=[10,20,30,40,50]
d=c.pop(2)
print(d)
